string FindString(unsigned long long p){
	arrit--;
	if(!arrit){
		if(p)
			return "1";
		else
			return "0";
	}
	if(p<arr[arrit])
		return '0'+FindString(p);
	else
		return '1'+FindString(arr[arrit+1]-p);
}
